#include <iostream>  
using namespace std; 

int main() { 

	int x = 1;  

	while (x < 11) {

		cout << x << endl;  
		if (x == 6) {
			cout << endl;
			break;
		}
		++x;  
	}

	x = 1;

	while (x < 11) {

		if (x == 6) {
			cout << endl;
			break;
		}
		cout << x << endl;  
		++x;  
	}

	x = 1;

	while (x < 11) {

		if (x == 6) {
			x++;
			continue;
		}
		cout << x << endl;  
		++x;  
	}

}


